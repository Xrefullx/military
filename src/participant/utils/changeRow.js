export const handleRowChange = (e, rowIndex,setTableData) => {
    const { name, value } = e.target;
    setTableData((prevData) => {
        const newData = [...prevData];
        newData[rowIndex] = {
            ...newData[rowIndex],
            [name]: value,
        };
        return newData;
    });
};

